

cabbage_crop = {

use = function(player)
	
	local item = player:getInventoryItem(player.invSlot)
	
	if player:hasItem(item.id, 1) == true then
		seed.use(player, ""..item.name)
	else
		broadcast(-1, ""..player.name.." is trying to hacking [Usable items]")
	end
end
}

